create table if not exists public.season_trade_proposals (
  id bigint generated by default as identity primary key,
  season_id integer not null references public.seasons(id) on delete cascade,
  created_by_player_id uuid references public.players(id) on delete set null,
  created_by_team_id integer not null references public.season_teams(id) on delete cascade,
  status text not null default 'pending' check (status in ('pending', 'accepted', 'rejected', 'cancelled')),
  created_at timestamptz not null default now(),
  resolved_at timestamptz
);

create table if not exists public.season_trade_proposal_teams (
  id bigint generated by default as identity primary key,
  season_id integer not null references public.seasons(id) on delete cascade,
  proposal_id bigint not null references public.season_trade_proposals(id) on delete cascade,
  team_id integer not null references public.season_teams(id) on delete cascade,
  decision_status text not null default 'pending' check (decision_status in ('pending', 'accepted', 'rejected', 'cancelled')),
  decided_at timestamptz,
  created_at timestamptz not null default now(),
  unique (proposal_id, team_id)
);

create table if not exists public.season_trade_proposal_moves (
  id bigint generated by default as identity primary key,
  season_id integer not null references public.seasons(id) on delete cascade,
  proposal_id bigint not null references public.season_trade_proposals(id) on delete cascade,
  roster_id integer not null references public.season_roster(id) on delete cascade,
  character_name text not null,
  from_team_id integer not null references public.season_teams(id) on delete cascade,
  to_team_id integer not null references public.season_teams(id) on delete cascade,
  created_at timestamptz not null default now()
);

create index if not exists season_trade_proposals_season_id_idx
  on public.season_trade_proposals (season_id, created_at desc);

create index if not exists season_trade_proposal_teams_season_id_idx
  on public.season_trade_proposal_teams (season_id, team_id, decision_status);

create index if not exists season_trade_proposal_teams_proposal_id_idx
  on public.season_trade_proposal_teams (proposal_id);

create index if not exists season_trade_proposal_moves_season_id_idx
  on public.season_trade_proposal_moves (season_id, proposal_id);

create index if not exists season_trade_proposal_moves_roster_id_idx
  on public.season_trade_proposal_moves (roster_id);
